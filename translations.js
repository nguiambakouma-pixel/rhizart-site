/* ---- TRANSLATION ENGINE ---- */

function applyTranslations() {
  const lang = currentLang;
  const dict = TRANSLATIONS[lang];
  if (!dict) return;

  document.documentElement.lang = lang;

  // Apply data-i18n to text content
  document.querySelectorAll('[data-i18n]').forEach(function (el) {
    const key = el.getAttribute('data-i18n');
    if (dict[key] !== undefined) {
      el.innerHTML = dict[key];
    }
  });

  // Apply data-i18n-placeholder for placeholders
  document.querySelectorAll('[data-i18n-placeholder]').forEach(function (el) {
    const key = el.getAttribute('data-i18n-placeholder');
    if (dict[key] !== undefined) {
      el.setAttribute('placeholder', dict[key]);
    }
  });

  // Apply data-i18n-value for input values
  document.querySelectorAll('[data-i18n-value]').forEach(function (el) {
    const key = el.getAttribute('data-i18n-value');
    if (dict[key] !== undefined) {
      el.setAttribute('value', dict[key]);
    }
  });

  // Update toggle active state
  document.querySelectorAll('.lang-btn').forEach(function (btn) {
    const btnLang = btn.getAttribute('data-lang');
    if (btnLang === lang) {
      btn.classList.add('active');
    } else {
      btn.classList.remove('active');
    }
  });

  // Dispatch event for other scripts (contact.js, devis.js)
  document.dispatchEvent(new CustomEvent('languageChanged', { detail: { lang: lang } }));
}

function setLanguage(lang) {
  if (lang === currentLang) return;
  currentLang = lang;
  localStorage.setItem('rhizart-lang', lang);
  applyTranslations();
}

/* ---- INIT ---- */
document.addEventListener('DOMContentLoaded', function () {
  // Apply initial translations
  applyTranslations();

  // Setup toggle listeners
  document.querySelectorAll('.lang-btn').forEach(function (btn) {
    btn.addEventListener('click', function () {
      const lang = this.getAttribute('data-lang');
      setLanguage(lang);
    });
  });

  // Listen for dynamic content changes (for contact.js/devis.js success messages)
  document.addEventListener('languageChanged', function () {
    // Re-apply any dynamic content if needed
  });
});